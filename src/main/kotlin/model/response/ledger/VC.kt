package model.response.ledger

import java.math.BigDecimal

/**
 * @property baseRate Exchange rate of the base pair. Each unit of the created curency will represent value of baseRate*1 basePair.
 * @property basePair Base pair for virtual currency. Transaction value will be calculated according to this base pair. e.g. 1 VC_VIRTUAL is equal to 1 BTC, if basePair is set to BTC.
 * @property customerId ID of customer associated with virtual currency.
 * @property description Used as a description within Tatum system.
 * @property name Virtual currency name. Must be prefixed with 'VC_'.
 * @property supply Supply of virtual currency.
 * @property accountId Virtual currency account.
 * @property erc20Address Address of ERC20 token.
 * @property issuerAccount Virtual currency account, on which initial supply was minted.
 * @property chain Blockchain, on which this virtual currency is paired on. Not present, when Tatum's private ledger is used as a base ledger.
 * @property initialAddress Ethereum address, where initial supply was minted.
 */

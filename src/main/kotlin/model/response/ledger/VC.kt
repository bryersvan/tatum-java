    val baseRate: BigDecimal,
    val basePair: BasePairEnum,
    val customerId: String? = null,
    val description: String,
    val name: String,
    val supply: String,
    val accountId: String,
    val erc20Address: String? = null,
    val issuerAccount: String? = null,
    val chain: String? = null,
